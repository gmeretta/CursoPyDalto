# creando tuplas con tuple
tupla = tuple(["dato1", "dato2"])
print(tupla)

# otra forma de crear la tupla
tupla2 = "datos1", "datos2"
print(tupla2)

# ojo
tupla3 = "dato1"
print(type(tupla3))
print(tupla3)

# agregar , cuando es una tupla de un solo dato
